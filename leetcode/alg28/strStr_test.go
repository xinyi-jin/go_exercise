package alg28

import "testing"

func Test_strStr(t *testing.T) {
	type args struct {
		haystack string
		needle   string
	}
	tests := []struct {
		name string
		args args
		want int
	}{
		{"Test_strStr_01", args{"sadbutsad", "sad"}, 0},
		{"Test_strStr_02", args{"leetcode", "leeto"}, -1},
		{"Test_strStr_03", args{"aaa", "aaaa"}, -1},
	}
	for _, tt := range tests {
		t.Run(tt.name, func(t *testing.T) {
			if got := strStr(tt.args.haystack, tt.args.needle); got != tt.want {
				t.Errorf("strStr() = %v, want %v", got, tt.want)
			}
		})
	}
}
